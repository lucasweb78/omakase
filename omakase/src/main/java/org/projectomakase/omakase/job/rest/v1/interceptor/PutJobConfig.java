package org.projectomakase.omakase.job.rest.v1.interceptor;

import javax.ws.rs.NameBinding;
import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * NameBinding annotation used to annotate JAX-RS Resource methods that should be intercepted by {@link PutJobConfigInterceptor}
 *
 * @author Richard Lucas
 */
@Target({ElementType.TYPE, ElementType.METHOD})
@Retention(value = RetentionPolicy.RUNTIME)
@NameBinding
public @interface PutJobConfig {
}
